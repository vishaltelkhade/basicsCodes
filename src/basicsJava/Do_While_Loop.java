package basicsJava;

public class Do_While_Loop {
public static void main(String[] args) {
	int i=1;
	int j=20;
	
	//print 1 to 10 using do while loop
do {
System.out.println(i);
     i++;
    
}
while(i<=10);


   // print 20 to 1 using do while loop
do {
System.out.println(j);
     j--;
}
while(j>=1);

}
}
