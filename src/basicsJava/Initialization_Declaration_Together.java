package basicsJava;

public class Initialization_Declaration_Together {
public static void main(String[] args) {
	
	//Syntax : Datatype Variable name = variable information;
	
	int Marks = 65;                    // declaration and Initialization together
	String StudentName = "Vishal";     // declaration and Initialization together
	char Grade= 'A';                   // declaration and Initialization together
	
      System.out.println("Marks ="+ Marks);               //Usage
      System.out.println("Student Name ="+StudentName);   //Usage
      System.out.println("Grade ="+Grade);                //Usage
}
}
