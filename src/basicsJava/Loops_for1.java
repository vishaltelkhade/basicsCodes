package basicsJava;

public class Loops_for1 {
public static void main(String[] args) {
	//even nos up to 20 using for loop
	
	
	System.out.println("even nos up to 20");
	for(int i=2;i<=20;i=i+2) {
	
		System.out.println(i);	
}
	//odd nos up to 20
	
	System.out.println("odd nos up to 20");
	for(int i=1;i<=20;i=i+2) {
		
		System.out.println(i);
	}
	System.out.println("loop completed");
}

}
