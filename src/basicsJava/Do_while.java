package basicsJava;

public class Do_while {
	public static void main(String[] args) {
		
	//backward odd nos using do while loop
int j=19;
int k=2;
int l=1;
do {
System.out.println(j);
j=j-2;
}
while(j>=1);

    //even nos up to 20 using do while loop
do {
	System.out.println(k);
	k=k+2;
}
while(k<=20);

    //1 to 20 nos using do while loop
do {
System.out.println(l);
l++;
}
while(l<=20);
}
}
